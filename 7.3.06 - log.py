'''Вам дан список с записями логов веб-сервера в формате (каждая строка):

IP-адрес - - [дата] "Метод URL HTTP/версия" Код_статуса Размер

Задача — из каждой строки извлечь IP-адрес и код статуса (например, 200,
404 и т.п.) и вывести их через пробел.
Входные данные:

Несколько строк — записи лога, до 1000 строк.
Выходные данные:

Для каждой входной строки выведите:
<IP> <код_статуса>

Sample Input:

192.168.1.1 - - [10/Jun/2023:13:55:36 +0000] "GET /index.html HTTP/1.1" 200 1024
10.0.0.2 - - [10/Jun/2023:13:56:01 +0000] "POST /login HTTP/1.1" 403 512
127.0.0.1 - - [10/Jun/2023:13:57:00 +0000] "GET /favicon.ico HTTP/1.1" 404 0

Sample Output:

192.168.1.1 200
10.0.0.2 403
127.0.0.1 404

'''

import re, sys

# Компилируем регулярное выражение для поиска IP-адреса и кода статуса

# ((?:.)+(?= - - )) — захватывает любые символы до подстроки " - - ", но не включает её
#        Это соответствует IP-адресу
# .+ — пропускает всё, что между IP и кодом статуса (дата, запрос и т.д.)
# ((?<=" )\d{3}) — ищет ровно 3 цифры (\d{3}), которые идут сразу после пробела и кавычки (" )
#        Это соответствует коду статуса (например, 200, 404)
regex = re.compile(r'((?:.)+(?= - - )).+((?<=" )\d{3})')

# Читаем каждую строку из ввода
for line in sys.stdin:
    # Применяем регулярное выражение к каждой строке (удаляем лишние пробелы и переносы через strip())
    for match in re.finditer(regex, line.strip()):
        # Выводим найденные группы: первую (IP) и вторую (код статуса), разделённые пробелом
        print(match.group(1), match.group(2))