'''На вход подается 2 массива на 1 строке. Элементы массивов разделены пробелом,
а сами массивы символом |. Необходимо найти пересечение этих массивов и вывести
его в отсортированном виде. Если пересечения нет, вывести Empty Result.'''

# Считываем строку и разделяем её на две части по разделителю ' | '
tmp = input().split(' | ')

# Преобразуем каждую часть в множество целых чисел
# Первая часть — первый массив, вторая — второй массив
numbers1, numbers2 = set(map(int, tmp[0].split())), set(map(int, tmp[1].split()))

# Находим пересечение двух множеств (общие элементы)
result = numbers1 & numbers2

# Если пересечение не пустое, выводим его в отсортированном порядке
# Иначе выводим 'Empty Result'
#print(*sorted(result) if result else 'Empty Result')

if result:
    print(*sorted(result))
else:
    print('Empty Result')