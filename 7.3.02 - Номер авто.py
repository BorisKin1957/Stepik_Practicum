'''

Необходимо определить, являются ли заданные строки корректными автомобильными
номерами формата, принятого в России:

Формат номера: X000XX000
где: ABEKMHOPCTYX

    X — заглавная латинская буква из множества: ABEKMHOPCTYX (аналог русских),

    0 — цифра от 0 до 9.

Описание формата:

    1 буква,

    3 цифры,

    2 буквы,

    2 или 3 цифры (регион).

Примеры допустимых номеров:

    A123BC77

    X000XX777

    M456OP99

Входные данные:

Несколько строк (по одной в каждой), каждая из которых может содержать предполагаемый номер.
Выходные данные:

Для каждой строки выведите "VALID", если строка соответствует формату корректного
номера, иначе — "INVALID".

Sample Input:

A123BC77
M456OP199
Z999ZZ77
A12BC77
K001AA7

Sample Output:

VALID
VALID
INVALID
INVALID
INVALID

'''

# Импортируем модули: re — для работы с регулярными выражениями, sys — для чтения ввода из стандартного потока
import re, sys

# Регулярное выражение для проверки формата автомобильного номера РФ
# \b — граница слова (гарантирует, что номер не является частью более длинной строки)
# [ABEKMHOPCTYX] — первая буква: одна из допустимых латинских букв, аналогичных русским
# \d{3} — три цифры подряд (от 0 до 9)
# [ABEKMHOPCTYX]{2} — две буквы из того же допустимого набора
# (?:\d{2}|\d{3}) — не захватывающая группа: либо 2, либо 3 цифры (код региона)
# \b — завершение границы слова
regex = r'\b[ABEKMHOPCTYX]\d{3}[ABEKMHOPCTYX]{2}(?:\d{2}|\d{3})\b'

# Читаем ввод построчно из стандартного потока (stdin)
for number in sys.stdin:
    if re.search(regex, number):  # Проверяем, соответствует ли строка регулярному выражению
        print('VALID')  # Если соответствует — выводим "VALID"
    else:
        print('INVALID')  # Иначе — "INVALID"