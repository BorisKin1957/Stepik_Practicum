'''На сетке размером m x n находится робот. Робот изначально расположен в левом
верхнем углу (то есть grid[0][0]). Робот пытается перейти в правый нижний угол
(то есть grid[m - 1][n - 1]). Робот может двигаться только вниз или вправо в любой момент времени.

Даны два целых числа m и n, верните количество возможных уникальных путей, по
которым робот может добраться до правого нижнего угла.'''

m, n = map(int, input().split())

# Инициализируем одномерный массив `matrix` длиной n, заполненный единицами
# matrix[j] будет хранить количество путей до клетки в текущей строке на позиции j
# Начальные значения — 1, потому что в первую строку можно добраться только вправо,
# и до любой клетки первой строки — ровно 1 путь
matrix = [1] * n

# Обрабатываем строки со 2-й по m-ю (индекс i от 1 до m-1, так как первая строка уже учтена)
for i in range(1, m):
    # Обрабатываем столбцы со 2-го по n-й (индекс j от 1 до n-1)
    # Первая колонка (j = 0) остаётся равной 1, потому что в неё можно попасть только сверху
    for j in range(1, n):
        # Количество путей в клетку (i, j) = сумма путей:
        # - сверху (matrix[j] — значение из предыдущей строки)
        # - слева (matrix[j - 1] — значение из текущей строки, уже обновлённое для j-1)
        # Так как мы используем одномерный массив, matrix[j] постепенно обновляется
        matrix[j] += matrix[j - 1]

# Нас интересует только matrix[n - 1])

print(matrix[n - 1])
