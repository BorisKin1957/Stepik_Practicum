'''

Напишите программу для расчёта стоимости звонка по следующим правилам:

    Вводятся времена начала и окончания разговора в 24-часовом формате

    Тарификация поминутная с округлением ВВЕРХ (неполная минута считается как полная)

    Стоимость минуты задаётся константой

    Необходимо вывести:

        Длительность разговора в минутах (с округлением)

        Стоимость звонка

Входные данные:

    Время начала в формате ЧЧ:ММ:СС (например, "14:30:15")

    Время окончания в формате ЧЧ:ММ:СС (например, "15:15:30")

    Стоимость одной минуты (вещественное число)

Выходные данные:

    Длительность в минутах (целое число с округлением вверх)

    Стоимость звонка (вещественное число, 2 знака после запятой)

Sample Input 1:

14:30:15
15:15:30
2.50

Sample Output 1:

46
115.00

Sample Input 2:

23:59:01
00:01:01
1.00

Sample Output 2:

2
2.00

Sample Input 3:

08:15:00
08:15:59
0.75

Sample Output 3:

1
0.75

'''

# Импортируем класс timedelta из модуля datetime
# timedelta позволяет работать с разницей между двумя моментами времени
from datetime import timedelta

# Считываем время начала звонка: часы, минуты, секунды
h1, m1, s1 = map(int, input().split(':'))
# Считываем время окончания звонка: часы, минуты, секунды
h2, m2, s2 = map(int, input().split(':'))

# Считываем стоимость одной минуты разговора
price = float(input())

# Создаём объект времени для начала звонка
start_of_call = timedelta(hours=h1, minutes=m1, seconds=s1)
# Создаём объект времени для окончания звонка
end_of_call = timedelta(hours=h2, minutes=m2, seconds=s2)

# Проверяем, пересекает ли звонок полночь (например, начался в 23:50, закончился в 00:10)
if start_of_call > end_of_call:
    # Если да, добавляем один день к времени окончания
    end_of_call += timedelta(days=1)

# Вычисляем продолжительность звонка
duration_of_call = end_of_call - start_of_call

# Преобразуем продолжительность в секунды (целое число)
total_seconds = int(duration_of_call.total_seconds())

# Вычисляем количество минут: округляем вверх до ближайшей минуты
minutes = (total_seconds + 59) // 60

# Выводим количество минут (целое число)
print(int(minutes))

# Форматируем вывод стоимости с двумя знаками после запятой
cost = minutes * price
print(f"{cost:.2f}")
