'''

В компании фиксируют время начала и окончания работы сотрудников в формате
часы и минуты. Иногда сотрудники делают перерывы: в течение дня может быть
несколько периодов работы (начало и конец каждого периода). По данным о
нескольких периодах работы одного сотрудника нужно посчитать общее время,
которое он провел за работой, в минутах.

Входные данные:
В первой строке целое число N (1 ≤ N ≤ 10) — количество рабочих периодов.
Далее идут N строк, каждая содержит по четыре числа: часы и минуты начала
периода, часы и минуты окончания периода (0 ≤ часы < 24, 0 ≤ минуты < 60).
Если время окончания меньше времени начала, значит период работы был с переходом
через полночь.

Выходные данные:
Одно целое число — суммарное время работы в минутах.

Sample Input 1:

3
9 00 11 30
13 15 15 45
22 00 01 00

Sample Output 1:

480

Sample Input 2:

2
8 00 12 00
13 00 17 00

Sample Output 2:

480

Sample Input 3:

1
23 30 00 15

Sample Output 3:

45

'''

# Импортируем класс timedelta из модуля datetime
# Он позволяет работать с разницей во времени (например, между двумя моментами)
from datetime import timedelta


# Функция для вычисления разницы между двумя моментами времени в секундах
def get_time_delta(h1, m1, h2, m2):
    # Создаем объект timedelta для времени начала работы
    time1 = timedelta(hours=h1, minutes=m1)

    # Создаем объект timedelta для времени окончания работы
    time2 = timedelta(hours=h2, minutes=m2)

    # Если время окончания меньше времени начала — значит, работа шла через полночь
    # В этом случае добавляем 1 день (24 часа) к времени окончания
    if time1 > time2:
        time2 += timedelta(days=1)

    # Вычисляем разницу между временем окончания и начала
    delta = time2 - time1

    # Возвращаем разницу в секундах (целое число)
    return int(delta.total_seconds())


# Переменная для хранения общей продолжительности работы в секундах
total_seconds = 0

# Считываем количество рабочих периодов
# Цикл повторяется N раз
for _ in range(int(input())):
    # Считываем строку, разбиваем на 4 числа: h1, m1 (начало), h2, m2 (окончание)
    h1, m1, h2, m2 = map(int, input().split())

    # Вычисляем продолжительность текущего периода в секундах и добавляем к общей сумме
    total_seconds += get_time_delta(h1, m1, h2, m2)

# Переводим общее количество секунд в минуты и выводим результат
print(total_seconds // 60)