'''Напишите программу, которая группирует слова по их анаграммам. Анаграмма —
это слово, образованное перестановкой букв другого слова.

Входные данные:

Первая строка содержит число n — количество слов.
Следующая строка содержит nn слов, разделенных пробелами.

Выходные данные:

Программа должна вывести группы анаграмм, каждая группа на новой строке.
Слова в каждой группе должны быть отсортированы в лексикографическом порядке.
Группы анаграмм должны быть отсортированы по первому слову в группе.

Sample Input 1:

6
eat tea tan ate nat bat
Sample Output 1:

ate eat tea
bat
nat tan
Sample Input 2:

3
listen silent enlist
Sample Output 2:

enlist listen silent
Sample Input 3:

4
hello world hell owrld
Sample Output 3:

hell
hello
owrld world
Sample Input 4:

4
abc cba cab bca
Sample Output 4:

abc bca cab cba'''

# не используется
_ = int(input())

# Считываем строку с n словами и разбиваем её на список слов
words = input().split()

# Создаём словарь, в котором ключом будет отсортированная строка букв (анаграммный ключ),
# а значением — список слов, которые являются анаграммами ключа
anagrams = {}

# Проходим по каждому слову из введённого списка
for word in words:
    # Сортируем буквы слова — это будет ключом для анаграмм

    key = ''.join(sorted(word))

    # Если такой ключ ещё не встречался, создаём пустой список для него
    if key not in anagrams:
        anagrams[key] = []

    # Добавляем текущее слово в группу анаграмм по соответствующему ключу
    anagrams[key].append(word)

# Формируем список групп: каждая группа — это отсортированный лексикографически список анаграмм
groups = [sorted(group) for group in anagrams.values()]

# Сортируем все группы по первому слову в каждой группе (лексикографически)
# Это нужно, чтобы вывести группы в требуемом порядке
groups.sort(key=lambda x: x[0])

# Выводим каждую группу анаграмм: слова через пробел

for group in groups:
    print(*group)
