'''На вход подается размер массива(гарантируется, что значение положительное)
и элементы массива на отдельной строке, разделенные пробелом.
Ваша задача вывести true, если все элементы в массиве одинаковые и false
в противном случае.'''

_ = input() # лишнее данное

# Считываем строку с элементами, разбиваем её по пробелам и сохраняем в список
words = input().split()

# Проверяем, что все элементы списка равны первому элементу
# Генерируем список булевых значений: True, если элемент равен первому, иначе False
# Функция all() возвращает True, если все значения в списке — True
# Используем кортеж ('false', 'true') и индекс для вывода результата: индекс 0 или 1
print(('false', 'true')[all([True if words[0] == word else False for word in words])])