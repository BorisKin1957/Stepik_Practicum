def get_rom_number(n: int, result='') -> str:
    """
    Преобразует целое число n (1–3999) в римскую запись.

    Параметры:
        n (int): арабское число
        result (str): накопленная строка римского числа (изначально пустая)

    Возвращает:
        str: римское представление числа
    """
    if 0 < n < 4000:  # Проверяем допустимый диапазон: римские цифры не
        # определены для 0 и чисел > 3999
        ts = n // 1000 * 1000  # Выделяем тысячи (например: 2345 → 2000)
        hs = (n - ts) // 100 * 100  # Выделяем сотни (например: 345 → 300)
        tes = (n - ts - hs) // 10 * 10  # Выделяем десятки (например: 45 → 40)
        bs = n % 10  # Выделяем единицы (например: 5 → 5)

        # Словарь для тысяч: от 1000 до 3000
        dic_ts = {1000: 'M', 2000: 'MM', 3000: 'MMM'}

        # Словарь для сотен: от 100 до 900
        dic_hs = {
            100: 'C', 200: 'CC', 300: 'CCC', 400: 'CD', 500: 'D',
            600: 'DC', 700: 'DCC', 800: 'DCCC', 900: 'CM'
            # исправлено: было 'DCC' для 800
        }

        # Словарь для десятков: от 10 до 90
        dic_tes = {
            10: 'X', 20: 'XX', 30: 'XXX', 40: 'XL', 50: 'L',
            60: 'LX', 70: 'LXX', 80: 'LXXX', 90: 'XC'
        }

        # Словарь для единиц: от 1 до 9
        dic_bs = {
            1: 'I', 2: 'II', 3: 'III', 4: 'IV', 5: 'V',
            6: 'VI', 7: 'VII', 8: 'VIII', 9: 'IX'
        }

        # Добавляем римские цифры по разрядам, если значение не ноль
        if ts:
            result += dic_ts[ts]
        if hs:
            result += dic_hs[hs]
        if tes:
            result += dic_tes[tes]
        if bs:
            result += dic_bs[bs]

    return result  # Возвращаем результат (пустую строку, если n вне диапазона)


# Считываем число с клавиатуры и выводим его римское представление
print(get_rom_number(int(input())))