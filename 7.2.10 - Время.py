'''На вход подается строка текста, в которой слова разделены пробелом.
Слова этой строки обозначают год, месяц, день, часы, минуты и секунды.
Ваша задача привести эту строку к формату D d-m-Y H:M:S'''

import time  # Импортируем модуль time для работы с временем и его форматированием

string = input()  # Считываем строку с клавиатуры, содержащую дату и время в формате: ГГГГ ММ ДД ЧЧ ММ СС

# Преобразуем строку в структурированный объект времени (struct_time)
# Формат '%Y %m %d %H %M %S' соответствует: год, месяц, день, часы, минуты, секунды
time_obj = time.strptime(string, '%Y %m %d %H %M %S')

# Форматируем объект времени в нужную строку:
# '%a' — сокращённое название дня недели (например, Mon)
# '%d-%m-%Y' — день-месяц-год
# '%H:%M:%S' — часы:минуты:секунды
result = time.strftime('%a %d-%m-%Y %H:%M:%S', time_obj)

print(result)  # Выводим отформатированную строку


