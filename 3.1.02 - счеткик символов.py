'''

На вход подается строка. Реализуйте метод сжатия строки на основе счетчика
повторяющихся символов. Например, строка aabcccccaaa должна превратиться в a2b1c5a3.
Если «сжатая» строка оказывается длиннее исходной, метод должен вернуть исходную строку.

Sample Input 1:

hhfffffroeeeee

Sample Output 1:

h2f5roe5'''

s = input()  # Считываем входную строку (например: "aaabbc")

new = s[0]  # Инициализируем строку с первым символом

# Проходим по оставшимся символам строки (начиная со второго)
for sym in s[1:]:
    if new[-1] == sym:  # Если текущий символ совпадает с последним в new
        new += sym  # Добавляем его — продолжаем группу одинаковых символов
    else:
        new += f' {sym}'  # Иначе начинаем новую группу, разделяя пробелом

# Теперь new содержит строку вида: "aaa b cc" (группы одинаковых символов через пробел)

l = new.split(' ')  # Разбиваем по пробелам → получаем список групп: ['aaa', 'b', 'cc']

new = ''  # Очищаем переменную для формирования результата

# Обрабатываем каждую группу
for el in l:
    if len(el) > 1:  # Если в группе больше одного символа
        new += f'{el[0]}{len(el)}'  # Добавляем символ и количество (например: 'a3')
    else:
        new += el  # Если один символ — просто добавляем его

print(new)  # Выводим результат (например: "a3b1c2")