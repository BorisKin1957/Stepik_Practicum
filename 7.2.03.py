'''На вход подается размер матрицы и ее элементы(каждый подмассив на
новой строке, элементы разделены пробелом). Ваша задача посчитать сумму
всех чисел на его границах и вывести в ответ получившееся число.
Ваша программа должна работать для любого размера массива типа 2х2, 3х3 и т.д.
Гарантируется, что массив будет иметь корректные размеры.'''

n = int(input())
matrix = [list(map(int, input().split())) for _ in range(n)]

result = 0

# Верхняя граница
for i in range(n):
    result += matrix[0][i]

# Нижняя граница
for i in range(n):
    result += matrix[n - 1][i]

# Правая граница (без углов)
for i in range(1, n - 1):
    result += matrix[i][n - 1]

# Левая граница (без углов)
for i in range(1, n - 1):
    result += matrix[i][0]

print(result)

