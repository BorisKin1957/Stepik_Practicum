'''

Вам дана строка с произвольным текстом, в котором могут встречаться
URL-адреса. Нужно извлечь все URL, которые соответствуют следующему
упрощённому формату:

    Начинаются с http:// или https://

    Далее идёт доменное имя (латинские буквы, цифры, дефисы и точки),
    например example.com или sub.domain123.org

    Опционально может быть путь, начинающийся с / и состоящий из любых
    символов, кроме пробелов

Программа должна вывести все найденные URL в порядке появления.
Входные данные:

Одна строка длиной до 1000 символов.
Выходные данные:

Все найденные URL, по одному в строке. Если URL нет — ничего не выводить.

Sample Input:

Посетите https://example.com или http://sub.domain.org/path/to/page.

Sample Output:

https://example.com
http://sub.domain.org/path/to/page

'''

import re

regex = re.compile(r'https?:\/\/\S+\b')

for match in re.finditer(regex, input()):
    print(match.group())
