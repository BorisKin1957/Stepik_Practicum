def get_pow(x: float, n: int, result=1) -> float:
    """
    Возведение числа x в степень n.

    Параметры:
        x (float): основание (число, которое возводим в степень)
        n (int): показатель степени (может быть отрицательным)
        result (float): накопленный результат; по умолчанию 1

    Возвращает:
        float: x^n, округлённое до 3 знаков после запятой.
               Если n отрицательное — возвращается 1 / (x^|n|)
    """
    # Вычисляем x в степени |n| (по модулю)
    for i in range(abs(n)):
        result *= x  # Умножаем result на x, n раз

    # Если степень положительная — возвращаем x^n
    if n > 0:
        return round(result, 3)

    # Если степень отрицательная — возвращаем 1 / (x^|n|)
    return round(1 / result, 3)


# Считываем входные данные и выводим результат, отформатированный до 3х
# знаков после запятой
print(f"{get_pow(float(input()), int(input())):.3f}")