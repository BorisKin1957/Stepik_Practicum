'''ребуется проверить, соответствует ли заданная строка требованиям по
сложности пароля.

Пароль считается корректным, если он удовлетворяет следующим условиям:

    Длина от 8 до 20 символов включительно.

    Содержит хотя бы одну заглавную латинскую букву (A-Z).

    Содержит хотя бы одну строчную латинскую букву (a-z).

    Содержит хотя бы одну цифру (0-9).

    Содержит хотя бы один специальный символ из набора: !@#$%^&*()-_+=.

Входные данные:

Несколько строк (каждая — один пароль).
Выходные данные:

Для каждой строки выведите "VALID", если пароль соответствует всем
требованиям, иначе — "INVALID".

Sample Input:

Password123!
password123!
PASSWORD123!
Pass12!
P@ssword123

Sample Output:

VALID
INVALID
INVALID
INVALID
VALID

'''


# Импортируем модуль sys для чтения ввода из стандартного потока
import sys

# Читаем каждую строку из стандартного ввода (по одной строке за итерацию)
for line in sys.stdin:
    flag = False  # Флаг, который будет True, если пароль удовлетворяет всем условиям
    # Словарь для отслеживания выполнения каждого из требований
    # dig — есть ли цифра, low — строчная буква, up — заглавная, sym — спецсимвол
    good = {'dig': 0, 'low': 0, 'up': 0, 'sym': 0}

    # Проверяем длину пароля: должна быть от 8 до 20 символов включительно
    if 7 < len(line.strip()) < 21:  # .strip() убирает символы перевода строки и пробелы по краям
        # Перебираем каждый символ в строке
        for char in line:
            if char.isdigit():  # Проверяем, является ли символ цифрой
                good['dig'] = 1  # Отмечаем, что цифра найдена
            elif char.isupper():  # Проверяем, является ли символ заглавной буквой
                good['up'] = 1   # Отмечаем, что заглавная найдена
            elif char.islower(): # Проверяем, является ли символ строчной буквой
                good['low'] = 1  # Отмечаем, что строчная найдена
            elif char in '!@#$%^&*()-_+=':  # Проверяем, является ли символ допустимым спецсимволом
                good['sym'] = 1  # Отмечаем наличие спецсимвола

            # Если все четыре требования выполнены, можно завершить цикл досрочно
            if sum(good.values()) == 4:
                flag = True  # Устанавливаем флаг, что пароль валидный
                break  # Выходим из цикла, дальнейшая проверка не нужна

    # Выводим "VALID", если все условия выполнены, иначе "INVALID"
    print('VALID' if flag else 'INVALID')



