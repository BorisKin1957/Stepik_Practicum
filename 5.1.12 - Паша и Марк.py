'''Паша и Марк играют в игру. Изначально есть число n.
Текущий игрок может выбрать любой делитель d числа n,
который удовлетворяет условию d<n. Далее этот игрок заменяет n на n−d.
Проигрывает тот, кто не может сделать ход. Определите, кто выиграет при
оптимальной игре. Первый ход всегда делает Паша. Выведите «Pasha», если
выиграет Паша, или «Mark», если победа за Марком.'''

# Чётность числа n определяет исход игры при оптимальной стратегии.
# Обоснование:
# - Если n чётное, то Паша может на каждом шаге вычитать 1 (если n не степень двойки — можно вычесть нечётный делитель),
#   но ключевой факт — при чётном n первый игрок может сохранять контроль над чётностью.
# - Однако известно, что эта игра эквивалентна игре с количеством простых множителей или с функцией Мёбиуса,
#   но на практике можно заметить закономерность:
#   - При чётном n Паша может вынудить выигрышную позицию.
#   - При нечётном n у первого игрока нет выбора, кроме как оставить чётное число (потому что все делители нечётного числа — нечётные, и n − d будет чётным).
#   А далее Марк оказывается в выигрышной позиции.

# Таким образом, если n нечётное — выигрывает Марк, если чётное — Паша.

n = int(input())

# Проверяем чётность числа n
# Если n чётное — выводим 'Pasha', иначе — 'Mark'
print('Mark' if n % 2 else 'Pasha')