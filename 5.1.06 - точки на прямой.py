'''На вход подается количество точек и далее на отдельных строках, разделенные
пробелом, координаты точек. Определите, лежат ли все эти точки на одной прямой.
Выведите «YES», если все точки лежат на одной прямой, и «NO», если нет.

Sample Input 1:

5
32 6
29 2
32 6
29 2
35 10
Sample Output 1:

YES
Sample Input 2:

2
-22 -75
-30 -70
Sample Output 2:

YES
Sample Input 3:

4
86 -93
88 -91
70 -81
86 -93
Sample Output 3:

NO'''

# Считываем количество точек
n = int(input())

# Если точек меньше 3, то они всегда лежат на одной прямой (через 2 точки всегда можно провести прямую)
if n < 3:
    print('YES')
else:
    # Создаем пустой список для хранения координат точек
    matrix = []
    # Считываем n строк, каждая содержит две координаты (x, y)
    for i in range(n):
        # Преобразуем ввод в список из двух целых чисел и добавляем в matrix
        matrix.append(list(map(int, input().split())))

    # Проверяем, лежат ли все точки на вертикальной прямой (все x-координаты одинаковы)
    count = 0
    for i in range(n - 1):
        # Если x-координата следующей точки равна x-координате текущей — увеличиваем счётчик
        if matrix[i + 1][0] == matrix[i][0]:
            count += 1
    # Если все x-координаты одинаковы (n-1 совпадений), точки лежат на вертикальной прямой
    if count == n - 1:
        print('YES')
        exit(0)  # Завершаем программу

    # Проверяем, лежат ли все точки на горизонтальной прямой (все y-координаты одинаковы)
    count = 0
    for i in range(n - 1):
        # Если y-координата следующей точки равна y-координате текущей — увеличиваем счётчик
        if matrix[i + 1][1] == matrix[i][1]:
            count += 1
    # Если все y-координаты одинаковы — точки лежат на горизонтальной прямой
    if count == n - 1:
        print('YES')
        exit(0)  # Завершаем программу

    # Сортируем точки по x (и по y, если x равны), чтобы можно было проверять последовательно
    matrix = sorted(matrix)

    # Убираем дубликаты точек: создаём новый список без повторений
    new_matrix = []
    for lst in matrix:
        if lst not in new_matrix:
            new_matrix.append(lst)

    # Проверяем, что все точки (после удаления дубликатов) лежат на одной прямой
    # Для этого сравниваем наклоны между соседними парами точек
    for i in range(1, len(new_matrix) - 1):
        # Вычисляем разности координат между точками (векторы)
        dx1 = new_matrix[i][0] - new_matrix[i - 1][0]  # Δx для первого отрезка
        dy1 = new_matrix[i][1] - new_matrix[i - 1][1]  # Δy для первого отрезка
        dx2 = new_matrix[i + 1][0] - new_matrix[i][0]  # Δx для второго отрезка
        dy2 = new_matrix[i + 1][1] - new_matrix[i][1]  # Δy для второго отрезка

        # Проверяем пропорциональность векторов: dy1/dx1 == dy2/dx2 → dy1 * dx2 == dy2 * dx1
        # Это избегает деления на ноль
        if dy1 * dx2 != dy2 * dx1:  # Если наклоны не совпадают
            print('NO')  # Точки не на одной прямой
            exit(0)  # Завершаем программу

    # Если все наклоны совпадают — точки на одной прямой
    print('YES')

