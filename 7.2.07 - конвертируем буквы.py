'''На вход подается строка текста, в которой слова разделены пробелом.
Ваша задача заменить строчные буквы на заглавные, а заглавные на строчные.
Дополнительным условием будет необходимость корректной работы
как с кириллицей, так и с латиницей.'''

# Считываем входную строку от пользователя
string = input()

# Инициализируем пустую строку для результата
result = ''

# Перебираем каждый символ в строке
for char in string:
    # Если символ — строчная буква, преобразуем её в заглавную
    if char.islower():
        result += char.upper()
    # Если символ — заглавная буква, преобразуем её в строчную
    elif char.isupper():
        result += char.lower()
    # Если символ не является буквой (например, пробел, знак препинания), оставляем как есть
    else:
        result += char

# Выводим итоговую строку с заменёнными регистрами
print(result)