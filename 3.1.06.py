'''

На вход подается размер массива и n-ое количество элементов.
Гарантируется, что размер массива будет больше нуля. Необходимо найти
в массиве подмассив с самой большой суммой. В ответ верните сумму этого подмассива.

Sample Input 1:

9
-2 1 -3 4 -1 2 1 -5 4

Sample Output 1:

6

Sample Input 2:

5
5 4 -1 7 8

Sample Output 2:

23

Sample Input 3:

1
1

Sample Output 3:

1

'''


n = int(input())

# Считываем массив чисел
num = list(map(int, input().split()))

# Инициализируем результат первым элементом
result = num[0]

# Перебираем все возможные начальные индексы подмассива
for j in range(n):  # j — начало подмассива
    # Перебираем все возможные конечные индексы (конец + 1, потому что срез [j:k] не включает k)
    for k in range(j + 1, n + 1):
        # Считаем сумму подмассива от j до k-1
        tmp = sum(num[j:k])
        # Обновляем максимальную сумму, если текущая больше
        if tmp > result:
            result = tmp

print(result)