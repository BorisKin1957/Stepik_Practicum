'''На вход подаётся количество элементов в массиве и n-ое количество элементов.
Необходимо найти минимальное положительное отсутствующее число
(например, для массива [1, 2, 0] это будет 3). В ответ вывести это число.'''

# не используется, так как далее читаем все числа из следующей строки
_ = input()

numbers = map(int, input().split())

# Фильтруем только положительные числа и сортируем их по возрастанию
only_pos = sorted([num for num in numbers if num > 0])

# Если в массиве нет положительных чисел, ответ: 1 (наименьшее положительное целое)
if not only_pos:
    print(1)
else:
    # Перебираем числа от 1 до максимального значения в массиве + 2
    # (max + 2 нужно, чтобы покрыть случай, когда все числа от 1 до max присутствуют)
    for i in range(1, only_pos[-1] + 2):
        # Как только находим число i, которого нет в массиве положительных чисел — выводим его и завершаем цикл
        if i not in only_pos:
            print(i)
            break