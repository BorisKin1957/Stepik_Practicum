'''

Вы играете в игру «Монополия++». В этой игре можно купить не более k зданий,
каждое из которых будет добавлять к вашему капиталу какую-то сумму.
Всего есть выбор из n зданий. Чтобы купить здание i, надо иметь текущий
капитал хотя бы ci. После покупки здание добавит в ваш текущий капитал
сумму pi. Изначально ваш капитал равен M(он задан в последней строке).
Определите, каким максимальным капиталом можно овладеть к концу игры.
В ответ выводите максимальный конечный капитал, который можно получить.

Sample Input 1:

5 3
1 1
3 3
8 10
4 1
1 2
1

Sample Output 1:

7

Sample Input 2:

2 1
1 10
0 20
1

Sample Output 2:

21

Sample Input 3:

2 2
2 2
3 3
1

Sample Output 3:

1

'''

# Считываем количество зданий n и максимальное количество покупок k
n, k = map(int, input().split())

# Список для хранения условий покупки каждого здания: [стоимость покупки ci, прибыль pi]
deal_options = []

# Считываем n строк с параметрами зданий
for i in range(n):
    # Каждая строка содержит два числа: ci (минимальный капитал для покупки) и pi (прибыль от здания)
    deal_options.append(list(map(int, input().split())))

# Считываем начальный капитал M
result = int(input())

# Массив для отслеживания, какие здания ещё можно купить (1 — доступно, 0 — уже куплено)
free_deals = [1] * n

# Пытаемся купить до k зданий (или меньше, если нет доступных)
for _ in range(k):
    # Словарь для хранения доступных сделок: ключ — индекс здания, значение — прибыль pi
    profit = {}

    # Перебираем все здания
    for i in range(n):
        # Проверяем, что здание ещё не куплено и что текущий капитал >= минимальной стоимости покупки
        if free_deals[i] and result >= deal_options[i][0]:
            # Если условия выполнены, добавляем это здание в словарь возможных сделок
            profit[i] = deal_options[i][1]  # pi — прибыль от здания

    # Если есть хотя бы одна доступная сделка
    if profit:
        # Находим индекс здания с **максимальной прибылью**
        best_index = max(profit.keys(), key=lambda x: profit[x])
        # Помечаем это здание как купленное
        free_deals[best_index] = 0
        # Добавляем прибыль от этого здания к текущему капиталу
        result += profit[best_index]
    else:
        # Если нет доступных для покупки зданий — выходим из цикла
        break

# Выводим итоговый капитал после всех возможных покупок
print(result)