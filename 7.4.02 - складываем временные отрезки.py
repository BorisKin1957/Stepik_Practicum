# Импортируем класс timedelta из модуля datetime
# timedelta позволяет работать с разницей между двумя моментами времени
from datetime import timedelta

# Создаём начальный временной отрезок равный нулю (0 часов, 0 минут, 0 секунд)
result = timedelta(hours=0, minutes=0, seconds=0)

# Считываем количество временных отрезков
# int(input()) — количество строк с временем, которые нужно обработать
for _ in range(int(input())):
    # Считываем строку, разбиваем её на части и преобразуем в целые числа
    # Ожидается, что ввод будет в формате "часы минуты секунды"
    h, m, s = map(int, input().split())

    # Создаём временной промежуток из введённых значений и прибавляем его к общему результату
    result += timedelta(hours=h, minutes=m, seconds=s)

'''Засада в том, что у типа timedelta нет атрибутов hours и minutes, 
позволяющих получить из result сразу же значения часов и минут. Достать часы и минуты 
можно через путь: всё в секунды, а потому уже секунды в результат'''
# Преобразуем общую сумму времени в общее количество секунд (целое число)
total_seconds = int(result.total_seconds())

# Вычисляем количество полных часов
hours = total_seconds // 3600

# Вычисляем количество оставшихся минут после вычета часов
minutes = (total_seconds % 3600) // 60

# Вычисляем оставшиеся секунды
seconds = total_seconds % 60

# Выводим итоговое время
print(hours, minutes, seconds)

