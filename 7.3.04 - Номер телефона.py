'''

Напишите программу, которая находит все телефонные номера в тексте и выводит
их по порядку появления.

Телефонный номер считается корректным, если он соответствует формату:

    Начинается с кода страны +7 или 8,

    Далее идёт 10 цифр,

    Между цифрами могут быть пробелы, дефисы или скобки, но не обязательно,

    Пример допустимых форматов:

        +7 912 345-67-89

        8(912)3456789

        89123456789

        +7-912-345-67-89

Программа должна вывести все корректные номера, приведённые к единому формату:
+7XXXXXXXXXX (только цифры, без пробелов и знаков).
Входные данные:

Одна строка с произвольным текстом длиной до 1000 символов.
Выходные данные:

Все найденные корректные номера, по одному в строке, в формате +7XXXXXXXXXX.
Если номеров нет — ничего не выводить.

Sample Input 1:

Позвоните нам: +7 912 345-67-89 или 8(495)1234567, но не +8 999 999 99 99.

Sample Output 1:

+79123456789
+74951234567

Sample Input 2:

Номера: 1234567890, +7912-345-67-89, 8 912 345 67 89

Sample Output 2:

+79123456789
+79123456789

'''

'''
Используем модуль re с флагом re.VERBOSE, чтобы можно было писать регулярное 
выражение с комментариями.
'''

import re


def find_and_format_phone_numbers(string):

    # Регулярное выражение:
    regex = r'''
        (?<!\+)               # Негативный просмотр назад: перед 8 не должно быть +
        (?:                   # Начало группы кода страны
            \+7               # Либо +7
            |                 # Или
            8                 # Просто 8
        )                     # Конец группы
        [ \-\(\)]*            # Разделители (пробел, дефис, скобки,  * --> могут отсутствовать)
        (\d{3})               # Код оператора (3 цифры)
        [ \-\(\)]*            # Разделители
        (\d{3})               # Следующие 3 цифры
        [ \-\(\)]*            # Разделители
        (\d{2})               # 2 цифры
        [ \-\(\)]*            # Разделители
        (\d{2})               # Последние 2 цифры
    '''

    # С помощью finditer находим все вхождения.
    matches = re.finditer(regex, string, re.VERBOSE)

    for match in matches:
        # Извлекаем цифровые группы: код оператора (3 цифры), следующие 3, потом 2 и 2.
        operator, three, two1, two2 = match.groups()
        # Форматируем в виде +7XXXXXXXXXX и выводим.
        number = f'+7{operator}{three}{two1}{two2}'
        print(number)


find_and_format_phone_numbers(input().strip())