'''На вход подается 2 строки - на первой строке шаблон, а на второй проверяемое
слово. В этой задаче вам надо определить, подходит ли строка под конкретный шаблон.
Шаблон задаётся в следующем формате:

Символ «?» соответствует одному вхождению любого символа;
Символ «*» соответствует произвольному числу любых символов, в том числе нулю символов;
Остальные символы шаблона должны совпадать с символами строки;
Выведите «YES», если строка подходит под шаблон, и «NO», если не подходит.

Sample Input 1:

*a*
adce
Sample Output 1:

YES
Sample Input 2:

*
xyz
Sample Output 2:

YES
Sample Input 3:

algorithms
algorithmus
Sample Output 3:

NO
Sample Input 4:

?
k
Sample Output 4:

YES
Sample Input 5:

??
k
Sample Output 5:

NO
Sample Input 6:

*a?
adce
Sample Output 6:

NO'''

# Импортируем функцию fullmatch из модуля re
# fullmatch проверяет, соответствует ли вся строка заданному регулярному выражению
from re import fullmatch

# Считываем первую строку — шаблон (может содержать символы '*', '?' и обычные символы)
sample = input().strip()

# Считываем вторую строку — слово, которое нужно проверить на соответствие шаблону
word = input()

# Переменная для хранения преобразованного регулярного выражения
regex = r''

# Словарь, где каждому специальному символу шаблона сопоставляется его аналог в регулярных выражениях:
# '*' → '.*' — соответствует любой последовательности символов (включая пустую)
# '?' → '.' — соответствует ровно одному любому символу
samples = {'*': '.*', '?': '.'}

# Проходим по каждому символу в строке-шаблоне
for char in sample:
    # Если символ — это '*' или '?', заменяем его на соответствующее регулярное выражение
    if char in samples:
        regex += samples[char]
    else:
        # Если это обычный символ, добавляем его в регулярное выражение "как есть"
        # (предполагается, что это буквенные или иные символы, которые должны совпадать точно)
        regex += char

# Проверяем, соответствует ли вся строка 'word' полученному регулярному выражению
# fullmatch возвращает объект совпадения, если строка целиком соответствует шаблону, иначе — None
# Выводим 'YES', если совпадение найдено, и 'NO' — если нет
print('YES' if fullmatch(regex, word) else 'NO')