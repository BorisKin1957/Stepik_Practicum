'''На вход подаются 2 слова на разных строках (гарантируется, что все символы
в нижнем регистре). Необходимо вернуть минимальное количество символов,
которые необходимо добавить в конец первой строки, чтобы она стала последовательностью.

Последовательностью можно считать строку, которая может быть получена из другой
строки путём удаления некоторых символов.

Sample Input 1:

coaching
coding
Sample Output 1:

4
Sample Input 2:

abcde
a
Sample Output 2:

0
Sample Input 3:

z
abcde
Sample Output 3:

5'''


word_1 = input().strip()
word_2 = input().strip()

# Ищем длину наибольшего куска word_2, который является подпоследовательностью word_1
i = 0  # указатель для word_2
for char in word_1:
    if i < len(word_2) and char == word_2[i]:
        i += 1

# Все символы word_2 до позиции i уже покрыты подпоследовательностью в word_1
# Осталось добавить только (len(word_2) - i) символов
result = len(word_2) - i

print(result)