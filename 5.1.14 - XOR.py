'''

На вход подается размер последовательности, а затем на следующей строке
сама последовательность, элементы которой разделены пробелом.
В этой задаче требуется по заданному массиву длины n найти два его
элемента xi и xj(1≤i≤j≤n), которые давали бы максимальное значение
функции xi⊕xj, где под ⊕ подразумевается операция побитового исключающего
«или», то есть xor. В ответ выводите максимальное значение xor-а,
которое можно получить.

Sample Input 1:

5
1 8 3 1 4

Sample Output 1:

12

Sample Input 2:

3
4 8 1024

Sample Output 2:

1032

Sample Input 3:

2
10 10

Sample Output 3:

0

'''


n = int(input())  # Считываем количество чисел в массиве

numbers = list(map(int, input().split()))  # Считываем массив чисел
result = 0  # Инициализируем переменную для хранения максимального значения XOR

# Перебираем все пары индексов (i, j), где i < j
for i in range(n):
    for j in range(i + 1, n):
        xor = numbers[i] ^ numbers[j]  # Вычисляем XOR двух элементов
        if xor > result:  # Если текущий XOR больше сохранённого результата
            result = xor  # Обновляем результат

print(result)  # Выводим максимальный XOR из всех пар