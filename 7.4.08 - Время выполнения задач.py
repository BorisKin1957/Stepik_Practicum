from datetime import timedelta

# Функция для вычисления разницы между двумя моментами времени в минутах
def get_delta(h1, m1, h2, m2):
    # Создаём объекты времени для начала и конца задачи
    time_1 = timedelta(hours=h1, minutes=m1)  # начальное время
    time_2 = timedelta(hours=h2, minutes=m2)  # конечное время

    # Если конечное время меньше начального — значит, событие пересекает полночь
    # Добавляем один день к конечному времени
    if time_1 > time_2:
        time_2 += timedelta(days=1)

    # Вычисляем разницу между временами
    delta = time_2 - time_1
    # Переводим разницу в секунды и получаем целое количество минут
    total = delta.total_seconds()

    # Возвращаем количество минут как целое число
    return int(total // 60)


# Переменная для хранения общей продолжительности всех задач
duration = 0

# Считываем количество событий
for _ in range(int(input())):
    # Читаем строку: название события, время начала и время окончания
    event, start, end = input().split()

    # Разбиваем время начала и окончания по двоеточию и преобразуем в числа
    h1, m1 = map(int, start.split(':'))  # час и минуты начала
    h2, m2 = map(int, end.split(':'))  # час и минуты окончания

    # Вычисляем продолжительность текущего события в минутах
    result = get_delta(h1, m1, h2, m2)

    # Выводим название события и его длительность в минутах
    print(f'{event}: {result} минут')

    # Добавляем длительность к общей сумме
    duration += result

# Переводим общее количество минут в часы и минуты
hour, minute = divmod(duration, 60)

# Выводим итоговое суммарное время
print(f'Общее время: {hour} часа {minute} минут')


