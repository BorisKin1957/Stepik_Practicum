'''

Необходимо написать программу, которая находит все даты в формате
ДД.ММ.ГГГГ в заданной строке и выводит их в порядке появления.
Дата считается допустимой, если:

    День — от 01 до 31,

    Месяц — от 01 до 12,

    Год — от 1000 до 2999.

Программа не обязана проверять, существует ли такая дата в календаре
(например, 31.02.2020 считается допустимой в рамках этой задачи).
Входные данные:

Одна строка длиной до 1000 символов, содержащая произвольный текст с
возможными датами в указанном формате.
Выходные данные:

Выведите все корректные даты, найденные в тексте, по одной в строке в
порядке появления. Если таких дат нет — ничего не выводить.

Sample Input 1:

Сегодня 01.01.2020, а завтра будет 02.01.2020. Старые записи от 15.11.1999 тоже важны.

Sample Output 1:

01.01.2020
02.01.2020
15.11.1999

Sample Input 2:

Некорректные: 32.01.2020, 12.13.2022. Корректная: 10.10.2010.

Sample Output 2:

10.10.2010

Sample Input 3:

В этом предложении нет дат вообще.

Sample Output 3: '''


import re


# Регулярное выражение для поиска дат в формате ДД.ММ.ГГГГ
regex = r'\b([0-2]\d|3[01])\.(0[1-9]|1[0-2])\.([1-2]\d{3})\b'

text = input()

for match in re.finditer(regex, text):
    print(match.group())
