'''У вас есть строка s, в которой находятся одно или несколько слов.
Каждая пара слов разделена пробелом. Строка t — анаграмма строки s, при этом i-е с
лово строки t может быть анаграммой только i-го слова строки s.
Например, "acb dfe" — анаграмма "abc def", а "def cab" и "adc bef" — нет.'''


def test_anagram(lst1: [str], lst2: [str]) -> bool:
    # Проверяем, одинаковое ли количество слов в обеих строках
    if len(lst1) != len(lst2):
        return False  # Если нет — строки не могут быть анаграммами по парам
    # Перебираем каждую пару слов по порядку (по индексу)
    for i in range(len(lst1)):
        # Сортируем буквы в словах: если слова являются анаграммами, их отсортированные версии совпадут
        if sorted(lst1[i]) != sorted(lst2[i]):
            return False  # Если хотя бы одна пара не совпадает — это не анаграммы
    # Если все пары прошли проверку, возвращаем True
    return True


words1 = input().split()
words2 = input().split()

# Вызываем функцию и выводим результат: True, если строки — анаграммы по парам, иначе False
print(test_anagram(words1, words2))

