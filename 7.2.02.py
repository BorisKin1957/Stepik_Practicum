'''На вход подается размер матрицы и ее элементы(каждый подмассив на новой
строке, элементы разделены пробелом). Ваша задача посчитать сумму его
диагоналей и вывести в ответ получившееся число. Ваша программа должна
работать для любого размера массива типа 2х2, 3х3 и т.д. Гарантируется,
что массив будет иметь корректные размеры'''


n = int(input())

# Считываем n строк матрицы, разбиваем каждую строку по пробелам и преобразуем элементы в целые числа
matrix = [list(map(int, input().split())) for _ in range(n)]

# Переменная для хранения суммы диагоналей
result = 0

# Проходим по индексам от 0 до n-1
for i in range(n):
    # Добавляем элемент из главной диагонали: matrix[i][i]
    # и элемент из побочной диагонали: matrix[n - 1 - i][i]
    result += matrix[i][i] + matrix[n - 1 - i][i]

print(result)