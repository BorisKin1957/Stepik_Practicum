'''


На вход подаётся число, которое является высотой треугольника Паскаля.
Необходимо реализовать этот треугольник. 

Sample Input 1:

5

[1]
[1, 1]
[1, 2, 1]
[1, 3, 3, 1]
[1, 4, 6, 4, 1]

'''

n = int(input())

# Создаём матрицу n x n, заполненную нулями
# Каждая строка — список из n нулей
matrix = [n * [0] for _ in range(n)]

matrix[0][0] = 1  # 1 — вершина треугольника

# Заполняем матрицу по правилу Паскаля:
# каждый элемент = сумме элемента сверху и элемента сверху-слева
for i in range(1, n):  # Начинаем со второй строки (индекс 1)
    for j in range(n):  # Проходим по каждому столбцу строки
        # matrix[i-1][j-1] — элемент "сверху-слева"
        # matrix[i-1][j]   — элемент "сверху"
        # Если j == 0, то j-1 выходит за границу, но Python вернёт 0 (из-за 0 в матрице)
        matrix[i][j] = matrix[i - 1][j - 1] + matrix[i - 1][j]

# Выводим только ненулевые элементы каждой строки
# Это нужно, потому что в конце строк много нулей (из-за прямоугольной матрицы)
for row in matrix:
    print([i for i in row if i])  # Фильтруем нули: оставляем только ненулевые значения