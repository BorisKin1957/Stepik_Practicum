'''
На вход подаётся 2 размера массивов и n-ое количество элементов для каждого массива. Необходимо написать программу, которая будет вычислять медиану для этих массивов. Массивы уже отсортированы. Гарантируется, что размер массива не будет отрицательным или равным нулю. В ответ вывести число.

Sample Input 1:

2
1
1 3
2
Sample Output 1:

2
Sample Input 2:

2
2
1 2
3 4
Sample Output 2:

2.5
Sample Input 3:

5
5
1 2 3 4 5
6 7 8 9 1001
Sample Output 3:

5.5

 Медиана массива — это значение из массива, которое делит отсортированный массив
 на две равные части: половина элементов меньше или равна медиане,
 другая половина — больше или равна.

Как правильно найти "медиану n массивов"?
Нужно:

Объединить все массивы в один.
Отсортировать полученный массив.
Найти медиану этого большого массива.'''

# Считываем количество элементов в первом и втором массиве
n, m = int(input()), int(input())

# Считываем первый массив как список целых чисел
matrix1 = list(map(int, input().split()))

# Считываем второй массив как список целых чисел
matrix2 = list(map(int, input().split()))

# Объединяем два массива в один
ext_matrix = matrix1 + matrix2

# Сортируем объединённый массив
ext_matrix.sort()

# Проверяем, чётная ли общая длина массива
if (n + m) % 2 == 1:
    # Нечётное количество элементов → медиана — элемент посередине
    # Индекс середины: (n + m) // 2 (т.к. индексация с 0)
    result = ext_matrix[(n + m) // 2]
else:
    # Чётное количество элементов → медиана — среднее двух центральных
    l = (n + m) // 2
    result = (ext_matrix[l - 1] + ext_matrix[l]) / 2  # индексы: l-1 и l


print(result)