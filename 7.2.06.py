'''На вход подается строка текста, в которой слова разделены пробелом.
Ваша задача посчитать количество заглавных и строчных букв в строке,
а так же общую длину строки. В ответ вывести ответ типа
<заглавные> <строчные> <общее количество> '''

string = input()  # Считываем входную строку от пользователя

# Словарь для хранения количества строчных и заглавных букв
result = {'lower': 0, 'upper': 0}
count = 0  # Счётчик общей длины строки (включая пробелы и другие символы)

# Перебираем каждый символ в строке
for char in string:
    if char.isupper():  # Проверяем, является ли символ заглавной буквой
        result['upper'] += 1  # Увеличиваем счётчик заглавных букв
    elif char.islower():  # Проверяем, является ли символ строчной буквой
        result['lower'] += 1  # Увеличиваем счётчик строчных букв
    count += 1  # Увеличиваем общий счётчик длины строки

# Выводим количество заглавных букв, строчных букв и общую длину строки
print(result['upper'], result['lower'], count)