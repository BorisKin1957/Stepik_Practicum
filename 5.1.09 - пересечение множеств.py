'''

На вход подаются данные для двух последовательностей,
описывающих отрезки в следующем виде:

    Количество отрезков 1
    Элементы отрезков 1, разделенные пробелом
    Количество отрезков 2
    Элементы отрезков 2, разделенные пробелом

Каждый отрезок задаётся координатой начала и конца — [start[i],end[i]].
Отрезки каждой последовательности отсортированы слева направо и не имеют общих точек.

Найдите пересечение двух последовательностей отрезков.
То есть третью последовательность отрезков, такую, что:

    Каждый отрезок содержится в некотором отрезке и первой,
    и второй последовательности;
    Никакой отрезок нельзя увеличить;
    Отрезки этой последовательности не имеют общих точек;
    Отрезки в последовательности также отсортированы в порядке возрастания.

Выведите по одному в строке отсортированные отрезки из пересечения последовательностей.
Заметьте, что длина отрезков в пересечении может быть нулевой,
в этом случае start[i]=end[i].

Sample Input 1:

3
1 4
5 10
15 16
2
0 2
4 5

Sample Output 1:

1 2
4 4
5 5

Sample Input 2:

1
1 4
1
1 4

Sample Output 2:

1 4

Sample Input 3:

1
1 4
2
5 7
9 11

Sample Output 3: '''

def get_sequence(n):
    """
    Считывает n отрезков и возвращает список множеств,
    где каждое множество — это все целые точки отрезка [start, end].
    """
    sequence = []
    for i in range(n):
        # Считываем начало и конец отрезка
        start, end = map(int, input().split())
        # Преобразуем отрезок в множество целых чисел от start до end включительно
        sequence.append(set(range(start, end + 1)))
    return sequence


# Считываем количество отрезков первой последовательности
n = int(input())
# Получаем первую последовательность как список множеств
sequence_1 = get_sequence(n)

# Считываем количество отрезков второй последовательности
n = int(input())
# Получаем вторую последовательность как список множеств
sequence_2 = get_sequence(n)

# Список для хранения результатов пересечения (будет содержать множества)
sequence_3 = []

# Перебираем каждый отрезок из первой последовательности
for item_1 in sequence_1:
    # Перебираем каждый отрезок из второй последовательности
    for item_2 in sequence_2:
        # Находим пересечение двух множеств (общие точки)
        confluence = item_1 & item_2
        # Если пересечение не пустое, добавляем его в результат
        if confluence:
            sequence_3.append(confluence)

# Преобразуем каждое множество в отсортированный список точек,
# чтобы можно было получить min и max (начало и конец отрезка)
sequence_3 = sorted([sorted(list(item)) for item in sequence_3])

# Выводим каждый отрезок: начало и конец
for item in sequence_3:
    print(item[0], item[-1])












