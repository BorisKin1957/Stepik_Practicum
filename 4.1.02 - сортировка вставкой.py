'''На первой строке дано целое число n (1 ≤ n ≤ 100) — количество элементов в
массиве. На второй строке задан сам массив: последовательность натуральных чисел,
не превышающих 10^9. Продемонстрируйте работу метода сортировки вставками по
возрастанию. Для этого выведите состояние массива после каждой вставки на
отдельных строках. Если массив уже упорядочен, ничего выводить не нужно.

Sample Input 1:

2
2 1
Sample Output 1:

1 2
Sample Input 2:

5
4 2 7 1 3'''


n = int(input())
numbers = list(map(int, input().split()))

# Основной цикл: начинаем со второго элемента (индекс 1)
# Первый элемент считается уже отсортированным
for i in range(1, n):
    # Сохраняем текущий элемент, который нужно вставить в отсортированную часть
    elem = numbers[i]

    # Начинаем проверять элементы слева (в уже отсортированной части массива)
    j = i - 1

    # Сдвигаем все элементы, которые больше elem, вправо
    # Условие: пока не вышли за границу (j >= 0) и текущий элемент больше elem
    while j >= 0 and numbers[j] > elem:
        numbers[j + 1] = numbers[j]  # сдвигаем элемент вправо
        j -= 1  # переходим к следующему элементу слева

    # Проверяем, изменилось ли место вставки по сравнению с оригинальным положением
    # если нет - продолжаем цикл
    if numbers[j + 1] == elem:
        continue
    # если да - вставляем elem в позицию
    else:
        numbers[j + 1] = elem
        # Выводим текущий результат на печать
        print(*numbers)


