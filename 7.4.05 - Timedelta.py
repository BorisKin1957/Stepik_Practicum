'''

Компания проводит онлайн-вебинары, для каждого из которых фиксируется время
начала и время окончания в формате часы и минуты. Вебинар может длиться с
переходом через полночь. За каждый полный час присутствия пользователя на
вебинаре ему начисляется 10 бонусных баллов, а за каждую полную минуту сверх
неполного часа — 1 балл. Нужно по времени подключения и отключения пользователя
посчитать, сколько бонусных баллов он заработал.

Входные данные:
В первой строке два целых числа — часы и минуты подключения пользователя
(0 ≤ часы < 24, 0 ≤ минуты < 60).
Во второй строке два целых числа — часы и минуты отключения пользователя
(0 ≤ часы < 24, 0 ≤ минуты < 60).

Выходные данные:
Одно целое число — количество начисленных бонусных баллов.

Sample Input 1:

21 20
23 50

Sample Output 1:

50

Sample Input 2:

10 00
10 59

Sample Output 2:

59

Sample Input 3:

12 00
12 00

Sample Output 3:

0

'''

# Импортируем класс timedelta из модуля datetime для работы с разницей во времени
from datetime import timedelta

# Чтение входных данных
h1, m1 = map(int, input().split())
h2, m2  = map(int, input().split())

# Создаём timedelta для обоих моментов
time1 = timedelta(hours=h1, minutes=m1)
time2 = timedelta(hours=h2, minutes=m2)

# Если второй момент меньше первого, то он на следующий день
# Поэтому добавляем 1 день к time2
if time2 < time1:
    time2 += timedelta(days=1)

# Вычисляем разницу
delta = time2 - time1

# Переводим общую разницу в секунды, затем вычисляем часы, минуты
total_seconds = int(delta.total_seconds())
hours = total_seconds // 3600
minutes = (total_seconds % 3600) // 60

# Выводим результат в баллах
print(hours * 10 + minutes)

